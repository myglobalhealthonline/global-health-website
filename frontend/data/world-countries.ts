/** Slugify a country name for URL use — lowercase, ascii-only, hyphenated. */
export function defaultSlugForCountry(name: string): string {
  return name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "") // strip accents (São → Sao)
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

/** Lookup helper used by the admin form. */
export function findCountryByIso(iso: string): WorldCountry | null {
  const lower = iso.toLowerCase().trim();
  return WORLD_COUNTRIES.find((c) => c.iso === lower) ?? null;
}
